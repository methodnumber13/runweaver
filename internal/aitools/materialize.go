func keepRuntimeBaselineFiles(root string, desiredAgents, desiredSkills map[string]bool, agents, skills []string) {
	for _, path := range agents {
		desiredAgents[filepath.Join(root, path)] = true
	}
	for _, path := range skills {
		desiredSkills[filepath.Join(root, path)] = true
	}
}

func writeGenerated(path, content string, force bool) error {
	if Exists(path) {
		data, err := os.ReadFile(path)
		if err != nil {
			return err
		}
		if !hasGeneratedMarker(data) {
			return nil
		}
	}
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		return err
	}
	return os.WriteFile(path, []byte(content), 0o644)
}
